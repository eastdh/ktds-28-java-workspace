package variables;

public class DecimalVariable {

  public static void main(String[] args) {
    // 코드 설명 작성 방법: 주석 (Comment) ==> Single line Comment
    // 정수 변수 생성 실습
    // byte, short, int, long
    byte byteNumber = 100; // 1byte --> 256 ==> -127~126
    short shortNumber = 3_300; // 2byte --> -32000~32000
    int intNumber = 300_000_000; // 4byte --> -21억~21억
    long longNumber = 4_000_000_000L; // 8byte --> -900경~900경

    // 변수의 이름은 숫자로 시작할 수 없음, 특수문자 삽입 불가

  }
}
